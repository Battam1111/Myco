"""ramify verb: scaffold a new verb / dimension / adapter.

Governing doctrine: ``docs/architecture/L2_DOCTRINE/surface.md``
(governance-verbs appendix). Governing craft:
``docs/primordia/v0_5_3_fungal_vocabulary_craft_2026-04-17.md``
(MAJOR 10 — the substrate-local extension seam).

ramify is the kernel's equivalent of hyphal branching: it extrudes a
new leaf module from the existing tree. Three modes:

1. ``--verb <name>`` — legacy scaffold (v0.5.0): writes a handler stub
   for a manifest-declared verb. Default target is
   ``src/myco/<subsystem>/<verb>.py`` when running against myco-self;
   otherwise writes under ``.myco/plugins/handlers/<verb>.py`` AND
   appends an entry to ``.myco/manifest_overlay.yaml``.

2. ``--dimension <id> --category <cat> --severity <sev>`` — scaffold
   a lint :class:`Dimension` subclass. Kernel-mode target is
   ``src/myco/homeostasis/dimensions/<id_lower>_<slug>.py``; substrate-
   local target is ``.myco/plugins/dimensions/<id_lower>.py`` plus
   autowiring of ``.myco/plugins/__init__.py`` to call
   :func:`register_external_dimension`.

3. ``--adapter <name> --extensions <e1,e2,...>`` — scaffold an
   ingestion Adapter class. Kernel-mode target is
   ``src/myco/ingestion/adapters/<name>.py``; substrate-local target
   is ``.myco/plugins/adapters/<name>.py``.

The ``--substrate-local`` flag overrides autodetection. The
autodetection rule: substrate-local mode is ON by default when the
substrate_id is NOT ``myco-self`` OR when ``<root>/src/myco/`` does
not exist. This preserves the v0.5.0 scaffold behavior for Myco-self
while giving downstream substrates a frictionless path.
"""

from __future__ import annotations

from pathlib import Path
from typing import Mapping

import yaml

from myco.core.context import MycoContext, Result
from myco.core.errors import ContractError, UsageError
from myco.surface.manifest import load_manifest

